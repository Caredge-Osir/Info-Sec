x=0
y=20
while y>=6:
    y-=4
    x+=(2/y)
# In each iteration, y is decreased by 4 and x is increased by the value of 2 divided by the new value of y.
print(x)
# The code initializes x to 0 and y to 20. It enters a while loop that continues as long as y is greater than or equal to 6.
print("End of program")